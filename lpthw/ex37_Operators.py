# // Floor division 2 // 4 == 0

# Also referred to as integer division. The resultant value is a
# whole integer, though the result’s type is not necessarily int.

# “Floor division” returns the “floor” of the result, or the 
# result of the division rounded down to the nearest integer


# %  modulus     2 % 4 == 2
# The modulo operator
# 
# it returns the remainder of a division operation, or 0 if there is no
# remainder. So 6%2 means find the remainder of 6 divided by 2.
#
# 7%2 evaluates to 1 because there's a remainder of 1 when 7 is divided 
# by 2 ( 3 times ).
# This is all integer based.

print(3 % 23450)
# 23450 goes into 3 zero times, with 3 remaining.

print(4 % 3)
# 3 goes into 4 1 time with 1 remaining.

print(7 % 4)
# 4 goes into 7 1 time with 3 remaining. 